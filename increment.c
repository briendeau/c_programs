// pre and post incrementing

#include <stdio.h>

int main( void )
{
  int c; // define var
 
 // demonstrate post increment
 c = 5; 
 printf( "%d\n", c ); //print 5
 printf( "%d\n", c++ ); // print 5 then postincrement
 printf( "%d\n", c ) ; // print 6

 //demonstrate preincrement
 c = 5; //asign 5 to c
 printf( "%d\n", c ); //print 5
 printf( "%d\n", ++c ); // preincrement then print 6
 printf( "%d\n", c ); //print 6
} // end function main

